package com.iron;

public class Demo1_加加问题 {

    public static void main(String[] args)
    {
        int i = 1;

        System.out.println("i: "  +   i);
        System.out.println("++i: "+   ++i);
        System.out.println("i++: "+   i++);
        System.out.println("i: "  +     i);
        System.out.println("--i: "+   --i);
        System.out.println("i--: "+   i--);
        System.out.println("i: "  +   i);
    }
}
